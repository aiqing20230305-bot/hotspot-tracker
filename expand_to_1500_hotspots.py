def generate_keywords_from_title(title, category_info, category_name):
    """从标题中提取关键词"""
    # 使用分类的行业词 + 标题中的关键词
    import re
    # 简单分词：按标点和空格分割
    words = re.findall(r'[\u4e00-\u9fa5]{2,6}', title)
    # 取标题中较有意义的词
    keywords = []
    for w in words:
        if len(w) >= 2 and w not in keywords and w not in ['引发', '成为', '话题', '热度', '市场', '行业', '成为新', '成为一']:
            keywords.append(w)
    # 补充分类相关关键词
    industry = random.choice(category_info['industries'])
    if industry not in keywords:
        keywords.insert(0, industry)
    # 保留前5个
    if len(keywords) < 5:
        keywords.append(category_name)
    return keywords[:5]

