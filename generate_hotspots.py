def generate_hotspot_data():
    """生成完整的热点数据"""
    hotspots = []
    id_counter = 51  # 从51开始，因为原有50条
    
    # 加载原有50条数据
    try:
        with open('hot_topics.json.backup', 'r', encoding='utf-8') as f:
            original_data = json.load(f)
            hotspots.extend(original_data)
    except:
        print("无法加载原始数据，将生成全新数据集")
    
    # 为每个分类生成热点数据
    for category_name, category_info in categories.items():
        for idx, (title, topic, keywords) in enumerate(category_info["topics"]):
            # 生成唯一ID
            hotspot_id = f"ht_202604040936_{str(id_counter).zfill(3)}"
            id_counter += 1
            
            # 随机选择平台和品牌
            platform = random.choice(category_info["platforms"])
            brands = random.sample(category_info["brands"], min(3, len(category_info["brands"])))
            
            # 生成热度值 (递减)
            hot_value = 390000000 - (id_counter - 51) * 500000 + random.randint(-1000000, 1000000)
            hot_value = max(hot_value, 100000000)  # 确保不低于1亿
            
            # 生成趋势标签
            trend_tags = [f"#{kw}" for kw in random.sample(keywords, min(3, len(keywords)))]
            
            # 随机趋势
            trends_options = [["热"], ["热", "新"], ["爆"], ["热"], ["新"]]
            trends = random.choice(trends_options)
            
            # 随机情感
            sentiment_options = ["正面", "中性", "正面"]
            sentiment = random.choice(sentiment_options)
            
            # 生成热度显示值
            heat_display = f"{hot_value / 100000000:.1f}亿"
            
            hotspot = {
                "id": hotspot_id,
                "title": title,
                "hot_value": hot_value,
                "platform": platform,
                "industries": [category_name, topic],
                "trends": trends,
                "type": category_info["type"],
                "sentiment": sentiment,
                "keywords": keywords,
                "c": brands,
                "created_at": base_time,
                "rank": id_counter - 50,
                "category": category_info["category"],
                "trend_tags": trend_tags,
                "url": platform.split("/")[0].lower() if "/" in platform else "weibo",
                "updated_at": base_time,
                "heat": heat_display,
                "logic": f"{category_name}话题热度上升",
                "trend": "稳定",
                "isNew": "新" in trends
            }
            
            hotspots.append(hotspot)
    
    # 按热度值排序
    hotspots.sort(key=lambda x: x["hot_value"], reverse=True)
    
    # 重新分配排名
    for idx, hotspot in enumerate(hotspots, 1):
        hotspot["rank"] = idx
    
    return hotspots

